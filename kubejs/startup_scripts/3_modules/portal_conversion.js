const portal_effects = {
	"minecraft:nether_portal": {
		particle: ["dragon_breath", 3, 0.06, 0.1],
		sound: ["block.fire.extinguish", 0.2, 2]
	}
};
function portal_conversion(entity) {
	if(entity.tags.contains("notp")) return false;
	const {item} = entity;
	const item_data = item_conversion[item.id];
	if(item_data) {
		const {id} = entity.block
		const potal_data = item_data[id];
		if(potal_data) {
			entity.addTag("notp");
			entity.setItem(Item.of(potal_data, item.count));

			const effect_data = portal_effects[id];
			if(effect_data) {
				const {level, x, y, z} = entity;
				const {particle, sound, behavior} = effect_data;
				if(particle) global.particleBurstBlock(level, x, y+0.125, z, particle[0], particle[1], particle[2], particle[3]);
				if(sound) global.sound(level, entity, sound[0], sound[1], sound[2]);
				if(behavior) behavior(entity);
			};

			return false;
		}
	};
	return true;
}